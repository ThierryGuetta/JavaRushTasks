package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            int x = 0;
            while (x < 10) {
                System.out.print("Q");
                x++;
            }
            System.out.println();
            i++;
        }
    }
}