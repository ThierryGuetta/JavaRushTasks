package MyTests;

public class ForTriangle001 {
    public static void main(String[] args) {
        for (int i = 0; i < 20; i++) {
            int strintCount = 20 - i;
            for (int j = 0; j < strintCount; j++) {
                System.out.print("b");
            }
            System.out.println();
        }
    }
}
